/*
3. Determine if higher-rated drivers tend to complete more trips and earn higher fares.
Is there a direct correlation between driver ratings and earnings?
*/

-- taking driver rating on bases of past 3 months 

select * from ps2;
with cte_ps3c as (
select
driverid, 
name,
ang_driver_rating as avg_driver_rating
from ps2
group by name 
order by avg_driver_rating desc
limit 5
)
,cte_fare_and_count as (
select
driverid, 
round(avg(fare),2) as avg_fare,
round(avg(distancekm),2) as avg_dist,
count(*) as no_of_rides
from bookings 
group by driverid )
select 
ps3.driverid,
ps3.name,
fc.avg_fare,
fc.avg_dist,
fc.no_of_rides,
round(ps3.avg_driver_rating,2) as last_3_M_driver_rating,
round(fc.no_of_rides*fc.avg_fare,2) as earning
from cte_ps3c as ps3
join cte_fare_and_count as fc 
using(driverid);

