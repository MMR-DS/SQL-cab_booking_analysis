2. Identify the top 3 most frequently traveled routes based on PickupLocation and
DropoffLocation. Should the company allocate more cabs to these routes?
*/
-- this are mfp and mfd on indiviual bases
with cte_booking as (
select 
BookingID,
PickupLocation,
DropoffLocation
from bookings
)
,cte_mfp as (
select 
PickupLocation,
count(*) as most_pick,
row_number() over(order by count(*) desc) as rnp
from cte_booking
group by pickuplocation
)
,cte_mfd as (
select 
dropoffLocation,
count(*) as most_drop,
row_number() over(order by count(*) desc) as rnd
from cte_booking
group by dropofflocation
)
select 
mfp.pickuplocation,
mfp.most_pick,
mfd.dropofflocation,
mfd.most_drop
from cte_mfp as mfp
join cte_mfd as mfd
on mfp.rnp = mfd.rnd 
order by rnp
limit 3 ;

-- top 3 routes 

select 
concat(coalesce(pickuplocation,"")," - ",coalesce(dropofflocation,"")) as route,
count(*) as rides_on_route
from bookings
group by route
order by rides_on_route desc
limit 3 ;

